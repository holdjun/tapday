interface LucideIconProps {
  name: string;
  className?: string;
  style?: CSSProperties;
  strokeWidth?: number;
}

export function LucideIcon({
  name,
  className,
  style,
  strokeWidth,
}: LucideIconProps) {
  const Icon = ICON_MAP[name];
  if (!Icon) return null;
  return <Icon className={className} style={style} strokeWidth={strokeWidth} />;
}
